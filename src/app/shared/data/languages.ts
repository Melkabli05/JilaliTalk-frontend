export type LanguageId = (typeof LANGUAGES)[number]['id'];

export function getLanguageById(id: number): LanguageEntry | undefined {
  return LANGUAGES.find((l) => l.id === id);
}

export function getLanguageName(id: number): string {
  return getLanguageById(id)?.name ?? 'Unknown';
}

export function getLanguageFlag(id: number): string {
  const lang = getLanguageById(id);
  if (!lang) return '';
  const code = lang.countryCode.toUpperCase();
  if (code.length !== 2) return '';
  const flag = [...code]
    .map((c) => String.fromCodePoint(c.charCodeAt(0) - 0x41 + 0x1F1E6))
    .join('');
  return flag;
}